package com.hendisantika.springbootjwt.service;

import com.hendisantika.springbootjwt.model.User;
import com.hendisantika.springbootjwt.model.UserDTO;
import com.hendisantika.springbootjwt.repository.UserRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.stereotype.Service;

import java.util.ArrayList;

